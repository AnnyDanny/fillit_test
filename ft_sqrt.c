#include "libft.h"

int	ft_sqrt(int nb)
{
	int i;

	if (nb <= 0)
		return (0);	
	while (nb)
	{
		i = 1;
		while (i <= nb / 2)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
		nb++;
	}
	return (0);
}
